def roll_dice(num_rolls, dice=six_sided):
    """Simulate rolling the DICE exactly NUM_ROLLS times. Return the sum of
    the outcomes unless any of the outcomes is 1. In that case, return 0.
    """
    # These assert statements ensure that num_rolls is a positive integer.
    assert type(num_rolls) == int, 'num_rolls must be an integer.'
    assert num_rolls > 0, 'Must roll at least once.'
    # BEGIN Question 1
    "*** REPLACE THIS LINE ***"
    # END Question 1
    outcomes = [dice() for i in range(num_rolls)]
    if 1 in outcomes:
        return 0
    return sum(outcomes)

def is_prime(n):
    """Check the input n is a prime or not.

    n: input integer.
    """
    assert type(n) == int, 'Input number must be an integer.'
    if n == 0 or n == 1:
        return False
    for i in range(2,n):
        if n % i == 0:
            return False
    return True
            
def next_prime(n):
    n = n + 1
    while not is_prime(n):
        n += 1
    return n

def take_turn(num_rolls, opponent_score, dice=six_sided):
    """Simulate a turn rolling NUM_ROLLS dice, which may be 0 (Free Bacon).

    num_rolls:       The number of dice rolls that will be made.
    opponent_score:  The total score of the opponent.
    dice:            A function of no args that returns an integer outcome.
    """
    assert type(num_rolls) == int, 'num_rolls must be an integer.'
    assert num_rolls >= 0, 'Cannot roll a negative number of dice.'
    assert num_rolls <= 10, 'Cannot roll more than 10 dice.'
    assert opponent_score < 100, 'The game should be over.'
    # BEGIN Question 2
    "*** REPLACE THIS LINE ***"
    # END Question 2
    tens, units, sum_score = opponent_score//10, opponent_score%10, 0
    if num_rolls == 0:
        sum_score = max(tens, units) + 1
    else:
        sum_score = roll_dice(num_rolls, dice)

    if is_prime(sum_score):
        sum_score = next_prime(sum_score)
    return sum_score
    

def select_dice(score, opponent_score):
    """Select six-sided dice unless the sum of SCORE and OPPONENT_SCORE is a
    multiple of 7, in which case select four-sided dice (Hog wild).
    """
    # BEGIN Question 3
    "*** REPLACE THIS LINE ***"
    # END Question 3
    total = score + opponent_score
    if total % 7 == 0:
        dice = four_sided
    else:
        dice = six_sided
    return dice


def get_digits(n):
    """Get the digits of the input nubmer n."""
    hundreds = n // 100
    tens = (n // 10) % 10
    units = n % 10
    return [hundreds, tens, units]


def is_swap(score0, score1):
    """Returns whether the last two digits of SCORE0 and SCORE1 are reversed
    versions of each other, such as 19 and 91.
    """
    # BEGIN Question 4
    "*** REPLACE THIS LINE ***"
    # END Question 4
    digits0 = get_digits(score0)
    digits1 = get_digits(score1)
    if digits0[1] == digits1[-1] and digits0[-1] == digits1[1]:
        return True
    else:
        return False


def other(player):
    """Return the other player, for a player PLAYER numbered 0 or 1.

    >>> other(0)
    1
    >>> other(1)
    0
    """
    return 1 - player

    
def play(strategy0, strategy1, score0=0, score1=0, goal=GOAL_SCORE):
    """Simulate a game and return the final scores of both players, with
    Player 0's score first, and Player 1's score second.

    A strategy is a function that takes two total scores as arguments
    (the current player's score, and the opponent's score), and returns a
    number of dice that the current player will roll this turn.

    strategy0:  The strategy function for Player 0, who plays first
    strategy1:  The strategy function for Player 1, who plays second
    score0   :  The starting score for Player 0
    score1   :  The starting score for Player 1
    """
    player = 0  # Which player is about to take a turn, 0 (first) or 1 (second)
    # BEGIN Question 5
    "*** REPLACE THIS LINE ***"
    # END Question 5
    DONE = False
    while not DONE:
        if player == 0:
            score0, score1 = get_outcome(strategy0, score0, score1)
            if is_swap(score0, score1):
                score0, score1 = score1, score0
            if score0 >= goal or score1 >= goal:
                DONE = True
                return score0, score1 
            player = other(player)
        if player == 1:
            score1, score0 = get_outcome(strategy1, score1, score0)
            if is_swap(score0, score1):
                score0, score1 = score1, score0 
            if score0 >= goal or score1 >= goal:
                DONE = True 
                return score0, score1
            player = other(player)


def get_outcome(strategy, my_score, opponent_score):
    dice_num = strategy(my_score, opponent_score)
    dice_type = select_dice(my_score, opponent_score)
    temp_score = take_turn(dice_num, opponent_score, dice_type)
    if temp_score == 0:
        opponent_score += dice_num
    my_score += temp_score
    return my_score, opponent_score

